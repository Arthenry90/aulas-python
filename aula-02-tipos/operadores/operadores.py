# Comentarios em #

"""
Triple quotes comenta 
varias linhas
"""

# Operador de + -> adição

print(10 + 10)

# Operador de - -> Subtração

print(20 - 30)

# Operador de * -> Multiplicação

print(4 * 5)

# Operador de / -> Divisão

print(10 / 5)

# Operador de // -> Divisão com inteiro

print( 3 // 2)

# Operador de % -> Modulo, oq resta

print(0 // 10, "esse")
